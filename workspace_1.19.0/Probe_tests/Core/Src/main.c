float conversao(){
	reset_1w(); //reset do sensor (a resposta de presença é retornada mas não avaliada).
	write_byte_1w(0xCC); //comando para pular ROM (só 1 dispositivo no barramento).
	write_byte_1w(0x44); //manda iniciar a conversão
	HAL_Delay(750);
	reset_1w();
	write_byte_1w(0xCC);
	write_byte_1w(0xBE); //avisa que quer ler a memória

	unsigned int temp_LSB = read_byte_1w();//só interesse em ler os dois bytes da temperatura
	unsigned int temp_MSB = read_byte_1w();//UNSIGNED INT??


	unsigned int temp_16bits = (temp_MSB<<8) | (temp_LSB);

	char sinal;
	float temperatura;

	if (temp_MSB > 7)
	{
	  temp_16bits = 0xFFFF - temp_16bits + 1;
	  sinal = '-';
	}
	else
	{
	  sinal = '+';
	}

	temperatura = (5*temp_16bits)/8; //dentro do else??
	return temperatura;
}

