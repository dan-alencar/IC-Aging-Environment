/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  MX_ADC1_Init();
  MX_RTC_Init();
  MX_USART1_UART_Init();

  /* Initialize power supply module */
  if (MYMGM1R_Init(&hi2c1, &MYMGM1_ctx) != PMBus_OK) {
    // Power module init failed - could add error handling here
    Error_Handler();
  }

  /* Calibrate ADC for temperature reading */
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);

  /* Initialize OLED display */
  OLED_Init();

  /* Initialize telemetry module */
  Telemetry_Init(&telemetry_ctx, &huart1, &MYMGM1_ctx, &hadc1);
  Telemetry_SetInterval(&telemetry_ctx, 500);  // 500ms interval
  Telemetry_Enable(&telemetry_ctx);

  /* Initialize UART bridge (PC ↔ FPGA ↔ STM32) */
#ifdef ENABLE_STLINK_DEBUG
  MX_USART2_UART_Init();
  Start_Bridge(&huart1, &huart2);
#else
  Start_Bridge(&huart1, NULL);

  /* Set UART2 pins to analog mode to reduce noise */
  GPIO_InitTypeDef GPIO_InitStruct = {0};
  __HAL_RCC_GPIOA_CLK_ENABLE();
  GPIO_InitStruct.Pin = GPIO_PIN_2 | GPIO_PIN_3;
  GPIO_InitStruct.Mode = GPIO_MODE_ANALOG;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);
#endif

  /* Initial voltage reading */
  MYMGM1R_Read_Vcore(&MYMGM1_ctx);
  HAL_Delay(500);

  /* Main loop - Display stats with integrated telemetry */
  Show_Stats_With_Telemetry(&MYMGM1_ctx, &hadc1, &telemetry_ctx);

  /* We should never reach here, but just in case */
  while (1)
  {
    /* Telemetry update in case Show_Stats returns */
    Telemetry_Update(&telemetry_ctx);
    HAL_Delay(100);
  }
}

